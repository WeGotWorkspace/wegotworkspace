import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { readFileSync, writeFileSync } from "node:fs";
import { buildOpenApiBuiltJson } from "./build-openapi-built-json.mjs";
import { generateSettingsRequestZod } from "./typegen-openapi-settings-zod.mjs";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(scriptDir, "..");
const generatedDir = path.resolve(packageRoot, "openapi/generated");
const builtDocPath = path.resolve(generatedDir, "openapi.built.json");
const openApiTypesPath = path.resolve(generatedDir, "openapi-types.ts");
const mailTypesPath = path.resolve(generatedDir, "mail-types.ts");
const notesTypesPath = path.resolve(generatedDir, "notes-types.ts");
const settingsTypesPath = path.resolve(generatedDir, "settings-types.ts");
const adminTypesPath = path.resolve(generatedDir, "admin-types.ts");
const driveTypesPath = path.resolve(generatedDir, "drive-types.ts");
const meetTypesPath = path.resolve(generatedDir, "meet-types.ts");
const installerTypesPath = path.resolve(generatedDir, "installer-types.ts");
const contactsTypesPath = path.resolve(generatedDir, "contacts-types.ts");

function buildDomainTypesModule({
  domainName,
  domainPrefix,
  pathPrefix,
  outputPath,
}) {
  const builtDoc = JSON.parse(readFileSync(builtDocPath, "utf8"));
  const schemaObj = builtDoc?.components?.schemas ?? {};
  const prefixes = Array.isArray(domainPrefix) ? domainPrefix : [domainPrefix];
  const schemaNames = Object.keys(schemaObj)
    .filter((name) => prefixes.some((prefix) => name.startsWith(prefix)) || name === "OkResponse")
    .sort((a, b) => a.localeCompare(b));

  const aliasLines = schemaNames.map(
    (name) => `export type ${name} = components["schemas"]["${name}"];`,
  );

  const fileContents = [
    "// Auto-generated by scripts/typegen-openapi-types.mjs. Do not edit manually.",
    "",
    'import type { components, paths } from "./openapi-types";',
    "",
    `export type ${domainName}Schemas = components["schemas"];`,
    `export type ${domainName}Paths = {`,
    `  [K in keyof paths as K extends \`/${pathPrefix}/\${string}\` ? K : never]: paths[K];`,
    "};",
    "",
    ...aliasLines,
    "",
    "export type { components, paths };",
    "",
  ].join("\n");

  writeFileSync(outputPath, fileContents, "utf8");
}

/**
 * Generate runtime OpenAPI JSON and TypeScript types for app domains.
 */
export async function generateOpenApiDomainTypes() {
  buildOpenApiBuiltJson();

  execFileSync("pnpm", ["exec", "openapi-typescript", builtDocPath, "-o", openApiTypesPath], {
    cwd: packageRoot,
    stdio: "inherit",
  });

  buildDomainTypesModule({
    domainName: "Mail",
    domainPrefix: "Mail",
    pathPrefix: "mail",
    outputPath: mailTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Notes",
    domainPrefix: "Note",
    pathPrefix: "notes",
    outputPath: notesTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Settings",
    domainPrefix: "Settings",
    pathPrefix: "settings",
    outputPath: settingsTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Admin",
    domainPrefix: ["Admin", "Update"],
    pathPrefix: "admin",
    outputPath: adminTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Drive",
    domainPrefix: "Drive",
    pathPrefix: "drive",
    outputPath: driveTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Meet",
    domainPrefix: "Meet",
    pathPrefix: "meet",
    outputPath: meetTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Installer",
    domainPrefix: "Installer",
    pathPrefix: "installer",
    outputPath: installerTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Contacts",
    domainPrefix: ["Contact", "AddressBook", "JsContact"],
    pathPrefix: "contacts",
    outputPath: contactsTypesPath,
  });

  generateSettingsRequestZod();
}

if (import.meta.url === new URL(process.argv[1], "file://").href) {
  await generateOpenApiDomainTypes();
  process.stdout.write(`Wrote ${builtDocPath}\n`);
  process.stdout.write(`Wrote ${openApiTypesPath}\n`);
  process.stdout.write(`Wrote ${mailTypesPath}\n`);
  process.stdout.write(`Wrote ${notesTypesPath}\n`);
  process.stdout.write(`Wrote ${settingsTypesPath}\n`);
  process.stdout.write(`Wrote ${adminTypesPath}\n`);
  process.stdout.write(`Wrote ${driveTypesPath}\n`);
  process.stdout.write(`Wrote ${meetTypesPath}\n`);
  process.stdout.write(`Wrote ${installerTypesPath}\n`);
  process.stdout.write(`Wrote ${contactsTypesPath}\n`);
  process.stdout.write(
    `Wrote ${path.resolve(packageRoot, "openapi/generated/settings-request-zod.ts")}\n`,
  );
}
