import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { readFileSync, writeFileSync } from "node:fs";
import { buildOpenApiBuiltJson } from "./build-openapi-built-json.mjs";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(scriptDir, "..");
const generatedDir = path.resolve(packageRoot, "openapi/generated");
const builtDocPath = path.resolve(generatedDir, "openapi.built.json");
const openApiTypesPath = path.resolve(generatedDir, "openapi-types.ts");
const mailTypesPath = path.resolve(generatedDir, "mail-types.ts");
const notesTypesPath = path.resolve(generatedDir, "notes-types.ts");

function buildDomainTypesModule({
  domainName,
  domainPrefix,
  pathPrefix,
  outputPath,
}) {
  const builtDoc = JSON.parse(readFileSync(builtDocPath, "utf8"));
  const schemaObj = builtDoc?.components?.schemas ?? {};
  const schemaNames = Object.keys(schemaObj)
    .filter((name) => name.startsWith(domainPrefix) || name === "OkResponse")
    .sort((a, b) => a.localeCompare(b));

  const aliasLines = schemaNames.map(
    (name) => `export type ${name} = components["schemas"]["${name}"];`,
  );

  const fileContents = [
    "// Auto-generated by scripts/typegen-openapi-types.mjs. Do not edit manually.",
    "",
    'import type { components, paths } from "./openapi-types";',
    "",
    `export type ${domainName}Schemas = components["schemas"];`,
    `export type ${domainName}Paths = {`,
    `  [K in keyof paths as K extends \`/${pathPrefix}/\${string}\` ? K : never]: paths[K];`,
    "};",
    "",
    ...aliasLines,
    "",
    "export type { components, paths };",
    "",
  ].join("\n");

  writeFileSync(outputPath, fileContents, "utf8");
}

/**
 * Generate runtime OpenAPI JSON and TypeScript types for app domains.
 */
export async function generateOpenApiDomainTypes() {
  buildOpenApiBuiltJson();

  execFileSync("pnpm", ["exec", "openapi-typescript", builtDocPath, "-o", openApiTypesPath], {
    cwd: packageRoot,
    stdio: "inherit",
  });

  buildDomainTypesModule({
    domainName: "Mail",
    domainPrefix: "Mail",
    pathPrefix: "mail",
    outputPath: mailTypesPath,
  });
  buildDomainTypesModule({
    domainName: "Notes",
    domainPrefix: "Note",
    pathPrefix: "notes",
    outputPath: notesTypesPath,
  });
}

if (import.meta.url === new URL(process.argv[1], "file://").href) {
  await generateOpenApiDomainTypes();
  process.stdout.write(`Wrote ${builtDocPath}\n`);
  process.stdout.write(`Wrote ${openApiTypesPath}\n`);
  process.stdout.write(`Wrote ${mailTypesPath}\n`);
  process.stdout.write(`Wrote ${notesTypesPath}\n`);
}
